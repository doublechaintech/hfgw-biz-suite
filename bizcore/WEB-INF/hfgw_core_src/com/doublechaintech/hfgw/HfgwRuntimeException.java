
package com.doublechaintech.hfgw;


public class HfgwRuntimeException extends RuntimeException {
    static final long serialVersionUID = -1;

    public HfgwRuntimeException() {
        super();
    }


    public HfgwRuntimeException(String message) {
        super(message);
    }


    public HfgwRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }


    public HfgwRuntimeException(Throwable cause) {
        super(cause);
    }


   
}









